package com.example.demo.service;

public class Useru {
    private String name;
    private int age;

    public Useru(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return this.name = name;
    }

    public int getAge() {
        return this.age = age;
    }
}
